package gr.aueb.cf.ch1;

/**
 * Prints the sum of
 * num1 + num2 as result
 */
public class ExThree {

    public static void main(String[] args) {

        // Declaration of variables.
        int num1 = 7;
        int num2 = 16;
        int result = 0;

        // Commands
        result = num1 + num2;

        // Printing of results
        System.out.printf("The result of %d, %d is equal to: %d\n", num1, num2, result);

    }
}
